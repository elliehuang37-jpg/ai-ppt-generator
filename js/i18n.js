function t(k, ...args) {
  let s = (I18N[UI_LANG] && I18N[UI_LANG][k] != null) ? I18N[UI_LANG][k] : (I18N.zh[k] != null ? I18N.zh[k] : k);
  args.forEach((a, i) => { s = s.replace("{" + i + "}", a); });
  return s;
}
function tLang(zh, en) { return UI_LANG === "en" ? (en || zh) : zh; }
// 英文模式下把中文 toast/loading 訊息換成英文（前綴比對）
function tMsg(msg) {
  if (UI_LANG !== "en" || !msg) return msg;
  if (TOAST_MAP[msg]) return TOAST_MAP[msg];
  for (const zh in TOAST_MAP) { if (msg.startsWith(zh)) return TOAST_MAP[zh] + msg.slice(zh.length); }
  return msg;
}
